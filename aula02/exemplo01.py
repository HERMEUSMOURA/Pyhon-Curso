#continuação de operadores aritmeticos(continuação)
valor1 = 50
valor2 = 500
print("exponenciação: ", valor1**3)
print("divisão padrão: ", valor2/valor1)
print("divisão sem decimais: ", valor1//valor2)
print("modulo(resto) da divisão:", valor2%2)

#operadores de comparação(relacional)
print("valor 1 e maior do que valor 2: ",valor1>valor2)
print("valor 1 e maior ou igual ao valor 2", valor1>=valor2)
print("valor 1 e menor que que o valor 2", valor1<valor2)
print("valor 1 e menor ou igual ao valor 2", valor1<=valor2)
print("valor 1 e diferente de valor 2", valor1!=valor2)
print("valor 1 e igual ao valor 2: ", valor1=valor2)